public class OperasiMatematika {
    public static void main(String[] args) {

        int number1 = 10;
        int number2 = 5;

        //augmented assignments
        number2 += 5;
        number1 -= 5;
        number1 *= 5;
        number1 /= 5;
        number1 %= 5;

        System.out.println(number2);

        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number1 / number2);
        System.out.println(number1 % number2);

    }

}
